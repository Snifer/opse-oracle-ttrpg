// src/core/random.ts
var Random = class {
  static d(sides) {
    return Math.floor(Math.random() * sides) + 1;
  }
  static roll2d6() {
    const d1 = this.d(6);
    const d2 = this.d(6);
    return { total: d1 + d2, d1, d2 };
  }
  static drawFocus(mode, deck) {
    if (mode === "cards" || mode === "persistent_deck") {
      const card = deck.draw();
      if (card.suit === "Joker" /* Joker */) {
        return { rank: "Joker", suit: "Joker" /* Joker */, wasJoker: true };
      }
      return { rank: card.rank, suit: card.suit };
    } else {
      const rankRoll = this.d(12);
      let rank = rankRoll.toString();
      if (rankRoll === 11) {
        rank = "J";
      }
      if (rankRoll === 12) {
        rank = this.d(2) === 1 ? "Q" : "K";
      }
      if (rankRoll === 1) {
        rank = "A";
      }
      const suitRoll = this.d(4);
      const suits = ["Clubs" /* Clubs */, "Diamonds" /* Diamonds */, "Spades" /* Spades */, "Hearts" /* Hearts */];
      return { rank, suit: suits[suitRoll - 1] };
    }
  }
  static pickOne(array) {
    return array[Math.floor(Math.random() * array.length)];
  }
};

